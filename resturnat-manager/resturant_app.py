from tkinter import *

# initiliaze TKinter

application = Tk()

# Window size
application.geometry('1020x630+0+0')

# Prevent from Mazimizing
application.resizable(False, False)

# Window title
application.title('VEGAN RESTURANT')

# background color
application.config(bg='burlywood')



# prevent window from closing
application.mainloop()


